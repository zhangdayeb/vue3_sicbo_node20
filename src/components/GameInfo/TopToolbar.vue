<script setup lang="ts">
import { ref, reactive, onMounted, onUnmounted } from 'vue'
import { useGameStore } from '@/stores/gameStore'

const gameStore = useGameStore()
const showSettings = ref(false)
const settingsDropdown = ref<HTMLElement>()

// 设置选项
const settings = reactive({
  backgroundMusic: true,
  soundEffects: true
})

// 生成局号
const generateGameNumber = () => {
  const tableId = 'T001'
  const now = new Date()
  const dateStr = now.getFullYear().toString().slice(-2) + 
                  String(now.getMonth() + 1).padStart(2, '0') + 
                  String(now.getDate()).padStart(2, '0')
  const sequence = String(gameStore.gameState.round).padStart(4, '0')
  return `${tableId}${dateStr}${sequence}`
}

const goBack = () => {
  console.log('返回上级页面')
}

const toggleSettings = () => {
  showSettings.value = !showSettings.value
}

// 功能跳转
const goToRecharge = () => {
  console.log('跳转到充值页面')
  showSettings.value = false
}

const goToVip = () => {
  console.log('跳转到会员中心')
  showSettings.value = false
}

const contactService = () => {
  console.log('联系客服')
  showSettings.value = false
}

const goToHelp = () => {
  console.log('跳转到帮助页面')
  showSettings.value = false
}

const refreshBalance = () => {
  console.log('刷新余额')
  // 这里可以调用API获取最新余额
  gameStore.updateBalance(gameStore.userBalance.total) // 模拟刷新
}

// 点击外部关闭下拉菜单
const handleClickOutside = (event: Event) => {
  if (settingsDropdown.value && !settingsDropdown.value.contains(event.target as Node)) {
    showSettings.value = false
  }
}

onMounted(() => {
  document.addEventListener('click', handleClickOutside)
})

onUnmounted(() => {
  document.removeEventListener('click', handleClickOutside)
})
</script>

